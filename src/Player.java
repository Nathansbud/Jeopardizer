public class Player {
    private String name;
    private int score;

    public Player() {

    }

    public String getName() {
        return name;
    }
    public void setName(String _name) {
        name = _name;
    }

    public int getScore() {
        return score;
    }
    public void setScore(int _score) {
        score = _score;
    }
}
